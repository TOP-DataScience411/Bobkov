from datetime import datetime, timedelta

def schelude(startdate: datetime, /, firstweekday: int, *otherweekday : int, summary: int, dateformat = "%y-%m-%d"):
    weekdays = [wd for wd in (firstweekday,) + otherweekday if 0 <= wd < 7]
    
    if weekdays == []:
        raise ValueError("У вас нет ни одной корректной недели")
    
    dates = []
    cdate = startdate
    while len(dates) < summary:
        if cdate.weekday() in weekdays:
            dates.append(cdate.strftime(dateformat))
        cdate += timedelta(days=1)
    
    return dates
    
