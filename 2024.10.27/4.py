def main():
    # КОММЕНТАРИЙ: что касается учебных задач, то обычно мы подразумеваем, что условный пользователь будет вводить требуемые данные всегда корректно — поэтому проверки на корректность вводимых данных можно не выполнять, пока в задании явным образом не сказано обратное
    # Ввод пользователя и проверка на трехзначность
    while True:
        number = int(input("Введите 3-значное число:"))
        if 100 <= number <= 999:
            break
        # ИСПОЛЬЗОВАТЬ: else блок не нужен 
        print("Ошибка: введите трехзначное число!")
    
    # Инициализация процесса
    sum_num = 0
    prod_num = 1
    nums = number
    
    # Извлекаем каждую цифру
    for _ in range(3):
       digit = nums % 10
       sum_num += digit
       prod_num *= digit
       nums //= 10

    # Вывод результата
    # ИСПРАВИТЬ: вывод не соответствует требуемому формату (неверные символы)
    # ИСПРАВИТЬ: необходимо справиться за один вызов функции print()
    # ИСПРАВЛЕНИЕ: Хорошо, один так один.
    print(f"Сумма цифр: {sum_num}\nПроизведение цифр: {prod_num}")
    
    return sum_num, prod_num, number


